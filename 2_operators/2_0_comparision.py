'''
comparison operators are used to compare the values of two operands.
when comparing strings, the comparison is based on alphabeticsl order of their characters.
'''
# equality operators (==)
'''
equality operator is used to check for equality.
It returns True is both the operands are equal, if both the left and right operands are equal to each other.
Otherwise, it returns False.
'''

a = 9
b = 5
c = 9
print(a == b) #False
print(a == c) #True

# Inequality operator (!=)
'''
The Not Equal To Operator returns True if both the operands are not equal and returns False if both the operands are equal.
'''
a = 9
b = 5
c = 9
print(a != b) #True
print(a != c) #False

# Greater than (>)
'''
The Greater Than Operator returns True if the left operand is greater than the right operand otherwise returns False.
'''
a = 5
b = 6
print(a > b) #False
print(b > a) #True

# Less than (<)
'''
The Less Than Operator returns True if the left operand is less than the right operand otherwise it returns False.
'''
a = 5
b = 6
print(a < b) #True
print(b < a) #False

# greater than or equal to (>=)
'''
The Greater Than or Equal To Operator returns True if the left operand is greater than or equal to the right operand, else it will return False.
'''
a = 9
b = 5
c = 9
print(a >= b) #True
print(a >= c) #True
print(b >= a) #False

# less than or equal to (<=)
'''
The Less Than or Equal To Operator returns True if the left operand is less than or equal to the right operand.
'''
a = 9
b = 5
c = 9
print(a <= b) #False
print(b <= c) #True
print(c <= b) #False


